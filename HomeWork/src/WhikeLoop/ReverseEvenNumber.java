package WhikeLoop;

public class ReverseEvenNumber {
	  public static void main(String[] args) {
	        int i = 50; // starting from the first even number

	        while (i >=2) {
	            System.out.println(i);
	            i -= 2; // increment by 2 to get the next even number
	        }
	    }
	

}
