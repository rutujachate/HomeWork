package WhikeLoop;

public class EvenNumber {
	
	    public static void main(String[] args) {
	        int i = 2; // starting from the first even number

	        while (i <= 50) {
	            System.out.println(i);
	            i += 2; // increment by 2 to get the next even number
	        }
	    }
	

}
