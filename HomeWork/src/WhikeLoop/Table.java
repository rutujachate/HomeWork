package WhikeLoop;

public class Table {
	
	    public static void main(String[] args) {
	        int number = 9; // number for the table
	        int i = 1; // starting multiplier

	        while (i <= 10) {
	            System.out.println(number + " x " + i + " = " + (number * i));
	            i++; // increment
	        }
	    }
	

}
