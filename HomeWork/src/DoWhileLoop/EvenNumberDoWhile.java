package DoWhileLoop;

public class EvenNumberDoWhile {
	
	    public static void main(String[] args) {
	        int i = 2; // start from the first even number

	        do {
	            System.out.println(i);
	            i += 2; // jump to the next even number
	        } while (i <= 50); // condition
	    }
	

}
