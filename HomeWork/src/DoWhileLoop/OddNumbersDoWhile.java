package DoWhileLoop;

public class OddNumbersDoWhile {
	
	    public static void main(String[] args) {
	        int i = 1; // start from the first odd number

	        do {
	            System.out.println(i);
	            i += 2; // jump to the next odd number
	        } while (i <= 50); // condition
	    }
	

}
