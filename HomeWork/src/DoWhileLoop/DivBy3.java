package DoWhileLoop;

public class DivBy3 {
	
	    public static void main(String[] args) {
	        int i = 3; // first number divisible by 3

	        do {
	            System.out.println(i);
	            i += 3; // jump to next multiple of 3
	        } while (i <= 30);
	    }
	

}
