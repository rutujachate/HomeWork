package ForLoop;

public class OddNumber {
	
	    public static void main(String[] args) {
	        System.out.println("Odd numbers from 1 to 100:");

	        // Loop from 1 to 100
	        for (int i = 1; i <= 100; i++) {
	            // Check if number is odd
	            if (i % 2 != 0) {
	                System.out.print(i + " ");
	            }
	        }
	    }
	

}
