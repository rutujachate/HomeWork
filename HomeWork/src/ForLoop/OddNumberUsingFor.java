package ForLoop;

public class OddNumberUsingFor {
	
	    public static void main(String[] args) {
	        System.out.println("Odd numbers from 1 to 100:");

	        // Start from 1 and increment by 2
	        for (int i = 1; i <= 100; i += 2) {
	            System.out.print(i + " ");
	        }
	    }
	}

