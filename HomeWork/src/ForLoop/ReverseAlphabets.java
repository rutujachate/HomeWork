package ForLoop;

public class ReverseAlphabets {
	
	    public static void main(String[] args) {
	        System.out.println("Alphabets from Z to A:");

	        // Loop from 'Z' to 'A'
	        for (char ch = 'Z'; ch >= 'A'; ch--) {
	            System.out.print(ch + " ");
	        }
	    }
	}


