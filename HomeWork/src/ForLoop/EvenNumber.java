package ForLoop;

public class EvenNumber {

	    public static void main(String[] args) {
	        System.out.println("Even numbers from 1 to 100:");

	        for (int i = 1; i <= 100; i++) {
	            // Check if the number is even
	            if (i % 2 == 0) {
	                System.out.print(i + " ");
	            }
	        }
	    }
	}


