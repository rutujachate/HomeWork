package ForLoop;

public class EvenNumberUsingFor {
	
	    public static void main(String[] args) {
	        System.out.println("Even numbers from 1 to 100:");

	        // Start from 2 and increment by 2
	        for (int i = 2; i <= 100; i += 2) {
	            System.out.print(i + " ");
	        }
	    }
	

}
